export function interpolate(template = '', params = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => String(params?.[key] ?? ''))
}

export function resolveLocale(value = '') {
  const normalized = String(value || '').trim()
  return SUPPORTED_LOCALES.find((item) => item.value === normalized)?.value || DEFAULT_LOCALE
}

export function getLocaleMetadata(value = '') {
  const locale = resolveLocale(value)
  return SUPPORTED_LOCALES.find((item) => item.value === locale) || SUPPORTED_LOCALES[0]
}

export function resolveMessage(messages = {}, key = '') {
  return String(key || '')
    .split('.')
    .filter(Boolean)
    .reduce((current, segment) => current?.[segment], messages)
}
